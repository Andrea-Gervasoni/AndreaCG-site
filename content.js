/* Merge: modifiche salvate dal pannello admin (localStorage) sovrascrivono i default */
(function () {
  function dm(a, b) {
    if (Array.isArray(b)) return b.slice();
    if (b && typeof b === 'object') {
      const o = Object.assign({}, a);
      for (const k in b) o[k] = dm(a && a[k], b[k]);
      return o;
    }
    return b === undefined ? a : b;
  }
  let stored = null;
  try { stored = JSON.parse(localStorage.getItem('acg-content')); } catch (e) {}
  window.ACG = stored ? dm(window.ACG_DEFAULT, stored) : window.ACG_DEFAULT;
  window.ACG_MERGE = dm;
})();
